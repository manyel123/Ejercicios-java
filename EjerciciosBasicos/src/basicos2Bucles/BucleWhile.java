/*
 Mostrar del 1 al 100 usando un bucle while
 */
package basicos2Bucles;

/**
 *
 * @author PC
 */
public class BucleWhile {
    
    public static void main(String[] args){
        
        int num = 1;
        
        while(num<=100){
            System.out.println(num);
            num++;
        }
    }
}
